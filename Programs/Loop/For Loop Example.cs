using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ForLoop
{
    class ForLoop
    {
        static void Main(string[] args)
        {
            //For loop
            for (int i = 1; i < 10; i++)
            {
                Console.WriteLine("value of i : " + i);
            }
            Console.ReadLine();
        }
    }
}